<strong>Helpful Hints..</strong><br>
<br>
&nbsp;&#149;&nbsp; Wi-Fi Protected Setup provides a more intuitive way of setting up wireless security
 between the router and the wireless client. Make sure the wireless card supports this feature or uses 
a certified Windows Vista driver in order to take advantage of this feature.
<br><br>
&nbsp;&#149;&nbsp; Changing your Wireless Network Name is the first step in securing your wireless network.
We recommend that you change it to a familiar name that does not contain any personal information.
<br><br>
&nbsp;&#149;&nbsp; Enabling Hidden Mode is another way to secure your network. With this option enabled,
no wireless clients will be able to see your wireless network when they perform a scan to see what's available. 
In order for your wireless devices to connect to your router, you will need to manually enter
the Wireless Network Name on each device.
<br><br>
&nbsp;&#149;&nbsp; If you have enabled Wireless Security, make sure you write down the WEP Key or Passphrase
that you have configured. You will need to enter this information on any wireless device that you connect
to your wireless network.
