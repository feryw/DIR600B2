<h1>Настройки Администратора</h1>
<p>
К интерфейсу управления можно получить доступ с учетной записи администратора ('admin') или пользователя ('user'). Учетная запись администратора дает права чтения/записи и возможность изменения паролей, в то время как учетная запись пользователя обеспечивает только привилегии чтения.


По умолчанию пароли не заданы. Поэтому в целях обеспечения безопасности маршрутизатора рекомендуется настроить пароли.
</p>
