<strong>Nützliche Hinweise</strong>
<br><br>
&nbsp;&#149;&nbsp; 
Klicken Sie auf die Schaltfläche <strong>„Speichern“</strong>, um die Protokolldatei auf einem 
lokalen Laufwerk zu speichern, damit diese später zur Fehlerbehebung an einen Netzwerkadministrator 
geschickt werden kann. Unter <strong>„Protokolltyp“</strong> können Sie auch auswählen, welche Art 
von Ereignis protokolliert werden soll.
<br>
<br>
&nbsp;&#149;&nbsp;
Bei einem System Logger (syslog) handelt es sich um einen Server, der die Protokolle von unterschiedlichen Quellen an einem Ort aufnimmt und ablegt.
Wenn das LAN einen Syslog-Server aufweist, können Sie diese Option dazu verwenden, die Protokolle des Routers an diesen Server zu senden.
