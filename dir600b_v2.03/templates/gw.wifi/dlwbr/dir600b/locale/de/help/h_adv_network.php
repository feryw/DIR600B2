<strong>Nützliche Hinweise</strong>
<br><br>
&nbsp;&#149;&nbsp; Für zusätzliche Sicherheit wird empfohlen, dass Sie die Option <strong>„Antwort auf WAN-Ping“</strong> 
deaktivieren. Ping wird oft von böswilligen Internetbenutzern verwendet, um aktive Netzwerke oder PCs im 
Internet aufzuspüren.
<br><br>
&nbsp;&#149;&nbsp; Wenn Sie Probleme mit dem Empfang von Video-on-Demand-Diensten aus dem Internet haben, überprüfen Sie, ob die Multicast-Streams-Option aktiviert ist.
