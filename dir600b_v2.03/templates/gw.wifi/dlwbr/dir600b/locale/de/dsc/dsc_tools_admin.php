<h1>Administratoreinstellungen</h1>
<p>
Die 'admin-' und 'Benutzerkonten' können auf die Managerment-Benutzeroberfläche zugreifen. Admin verfügt über Zugriffsberechtigungen zum Lesen/Schreiben, während ein Benutzer nur über schreibgeschützten Zugriff verfügt.
Standardmäßig ist kein Kennwort konfiguriert. Es wird dringend empfohlen, ein Kennwort zu erstellen, um Ihren Router zu sichern.
</p>
