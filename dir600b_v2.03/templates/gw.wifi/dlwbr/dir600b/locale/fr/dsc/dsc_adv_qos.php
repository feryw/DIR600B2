<h1>QOS ENGINE</h1>
<p>
Cette section vous aidera à configurer le moteur de qualité de service de D-Link qui s'appuie sur StreamEngine (™ technologie). Le moteur QoS améliore la pratique de divertissements en ligne. Il donne la priorité au trafic de jeu plutôt quà dautres transmissions réseau (FTP, Web, etc.).
