<h1>Paramètres administrateur</h1>
<p>
Les comptes "Admin" et "Utilisateur" peuvent accéder à l'interface de gestion. L’admin possède un accès de lecture/écriture et peut modifier les mots de passe, alors que l’utilisateur possède un accès en lecture seule.
Par défaut, aucun mot de passe n'est configuré. Il est vivement recommandé d'en créer un pour que votre routeur reste sécurisé.
</p>
