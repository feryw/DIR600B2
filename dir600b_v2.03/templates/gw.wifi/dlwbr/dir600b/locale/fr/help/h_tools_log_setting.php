Cliquez sur le bouton <strong>Enregistrer</strong> pour sauvegarder le fichier journal sur votre disque dur local. 
Vous pourrez l'envoyer par la suite à l'administrateur réseau à des fins de dépistage des pannes. 
Vous pouvez également sélectionner le type d'événement à consigner en définissant l'option Type & Niveau de journal.
<br>
<br>
&nbsp;&#149;&nbsp; 
Un système enregistreur (syslog) est un serveur qui collecte les journaux provenant de différentes sources.
Si le réseau local inclut un serveur syslog, vous pouvez utiliser cette option pour envoyer les journaux du routeur vers ce serveur. 
