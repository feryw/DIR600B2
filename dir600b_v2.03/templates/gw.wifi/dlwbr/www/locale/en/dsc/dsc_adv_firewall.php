<h1>FIREWALL & DMZ SETTINGS</h1>
<p>
Firewall rules can be used to allow or deny traffic passing through the router. 
You can specify a single port by utilizing the input box at the top or a range of 
ports by utilizing both input boxes.
</p>
<p>
DMZ means "Demilitarized Zone".
DMZ allows computers behind the router firewall to be accessible to Internet traffic.
Typically, your DMZ would contain Web servers, FTP servers and others.
</p>
