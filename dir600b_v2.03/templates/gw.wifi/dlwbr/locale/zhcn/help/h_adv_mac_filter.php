&nbsp;&#149;&nbsp; 创建一个MAC地址列表，您可以允许或拒绝这些地址访问您的网络。
<br><br>
&nbsp;&#149;&nbsp; 从路由器的DHCP服务器获取IP地址的计算机将会在DHCP客户端列表中。从下拉菜单中选择一个设备然后点击箭头将该设备的MAC地址添加到列表中。
<br><br>
&nbsp;&#149;&nbsp; 使用左边的复选框来选择是开启或禁用特定的条目。
<br><br>
&nbsp;&#149;&nbsp; 
如果您在路由器上预设了计划表，请使用<strong>"总是"</strong>下拉菜单。
如果没有，点击<strong>"添加新的"</strong>按钮来添加。
