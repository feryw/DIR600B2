<strong>帮助提示</strong>
<br><br>
&nbsp;&#149;&nbsp; 
在<strong>应用程序名称</strong>下拉菜单中查看预先设置好的您可以选择的应用程序。如果您选择了预先定义好的应用程序，点击下拉菜单旁边的箭头按钮填写适当的地方。
<br><br>
&nbsp;&#149;&nbsp; 
您可以在<strong>计算机名称</strong>下拉菜单中的DHCP客户端列表处选择您的计算机，或者手动输入您的计算机IP。
<br><br>
&nbsp;&#149;&nbsp; 
该功能允许您在网络上向一台计算机打开一系列端口。要这样做，在<strong>公共端口</strong>下面的第一栏输入你想打开的一系列端口的第一个端口号，然后在第二栏输入最后一个。之后您需要在<strong>私人端口</strong>下面的第一栏中输入你想打开的一系列端口的第一个端口号，然后在第二栏输入最后一个。
<br><br>
&nbsp;&#149;&nbsp; 要使用该功能打开单个端口，只需在两栏都输入相同的端口号就可以了。
