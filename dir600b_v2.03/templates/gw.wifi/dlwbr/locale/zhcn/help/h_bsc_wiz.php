<strong>帮助提示</strong><BR>
<BR>
<STRONG>无线设置</STRONG><BR>
无线设置部分用于对路由器的无线AP（接入点）进行配置。注意在这部分做的一些修改可能需要匹配一些无线客户端设备。 <BR>
<BR>
<STRONG>无线电</STRONG><BR>
该选项可以打开或者关闭路由器的无线连接功能。无线电打开时，显示下面的无线参数。 <BR>
<BR>
<STRONG>SSID</STRONG><BR>
服务集标识符（SSID）是一个识别特殊无线局域网络的名称。当无线设备正在浏览合适的无线网络时，该名称就会出现在列表中。<BR>
<BR>
<STRONG>自动信道选择</STRONG><BR>
如果选择该项，该路由器会自动找到干扰最小的信道，并通过这个信道建立无线连网。如果您禁用该选项，该路由器会使用您指定的信道。
