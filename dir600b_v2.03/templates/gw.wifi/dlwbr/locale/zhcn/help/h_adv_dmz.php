<strong>帮助提示</strong><br>
<br>
&nbsp;&#149;&nbsp; 开启DMZ选项仅作为最后一个办法。如果您在路由器下面使用某个应用程序有困难的时候，首先请尝试在虚拟服务器或转发端口中打开与应用程序相关的端口。<br>
<br>
&nbsp;&#149;&nbsp; 如果您想在路由器下面使用VPN客户端，请确保VPN通道已被开启。<br>
<br>
&nbsp;&#149;&nbsp; VPN通道只有在正在被使用的VPN客户端在与VPN连接类型相关的标准端口上运行时才会生效。如果您在路由器后使用VPN连接类型有困难，而VPN又是开启的话，请联系您的网络管理员以查看是否有非标准的端口或选项正在被使用。<br>
