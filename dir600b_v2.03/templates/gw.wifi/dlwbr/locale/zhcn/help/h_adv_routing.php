<strong>帮助提示</strong>
<br>
<p>&nbsp;&#149;&nbsp;开启：<br>
指定条目是否被开启或禁止。</p>

<p>&nbsp;&#149;&nbsp;接口：<br>
指定接口—WAN或物理WAN—当路由器在使用时，IP数据包必须被发送出路由器。</p>

<p>&nbsp;&#149;&nbsp;目的IP：<br>
将使用此路由器的数据包的IP地址。</p>

<p>&nbsp;&#149;&nbsp;子网掩码：<br>
掩码中的字节指定IP地址的字节必须匹配。</p>

<p>&nbsp;&#149;&nbsp;网关：<br>
指定路由器将使用的下一次跳数。0.0.0.0的网关表示没有跳数，匹配的IP地址在指定的WAN或WAN物理接口上直接连接到路由器。</p>
