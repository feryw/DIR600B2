<strong>帮助提示</strong><br>
<br>
&nbsp;&#149;&nbsp; Wi-Fi保护安装在路由器和无线客户端之间提供了更直观的无线安全安装方式。请确定无线卡支持这种特性或者使用一个合格的Windows Vista驱动程序来运用这个特性。
<br><br>
&nbsp;&#149;&nbsp; 为了使您的无线网络更安全，对您的无线网络名称进行更改是采取的第一个步骤。我们建议您将它更改成一个熟悉的名称，并且不包含任何个人信息。
<br><br>
&nbsp;&#149;&nbsp; 启用隐藏模式是保护您的网络的另一种方式。启用这个选项后，无线客户端在进行扫描查看可用的网络时，也看不见您的无线网络。为了使您的无线设备能连接到路由器上，您需要在每台设备上都要手动输入无线网络名称。
<br><br>
&nbsp;&#149;&nbsp; 如果您启用无线安全，请确定您写下了您已经配置好了的WEP密钥或者短语密码。您还需要输入任意一台连接到无线网络的无线设备的信息。
