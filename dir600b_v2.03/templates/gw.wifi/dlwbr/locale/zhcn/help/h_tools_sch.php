<strong>帮助提示</strong><br>
<br>
&nbsp;&#149;&nbsp; 当一些特性生效时，使用调度进程的一些其它特性对其下定义。<br><br>
&nbsp;&#149;&nbsp; 给每个调度进程都定义一个对您来说比较有意义的名称。比如，从星期一至星期五，3：00pm至9：00pm，可以取名为&quot;After School&quot;。<br><br>
&nbsp;&#149;&nbsp; 点击<strong>保存设置</strong>，将一张调度进程添加到下面的列表中。<br><br>
&nbsp;&#149;&nbsp; 点击<strong>编辑</strong>图标，对现有的调度进程进行编辑。<br><br>
&nbsp;&#149;&nbsp; 点击<strong>删除</strong>图标，永久删除这个调度进程。
