<strong>Nützliche Hinweise</strong><BR>
<BR>
<STRONG>WLAN-Einstellungen</STRONG><BR>
Im Abschnitt WLAN-Einstellungen können Sie den drahtlosen AP (Access Point) des Routers konfigurieren. Beachten Sie, dass einige Änderungen, die Sie in diesem Abschnitt vornehmen, ggf. auf den drahtlosen Clients wiederholt werden müssen. <BR>
<BR>
<STRONG>Drahtlosfunktion</STRONG><BR>
Über diese Option kann die WLAN-Funktion des Routers aktiviert bzw. deaktiviert werden. Wenn die Drahtlosfunktion aktiviert ist, werden die folgenden WLAN-Parameter angezeigt. <BR>
<BR>
<STRONG>SSID</STRONG><BR>
Service Set Identifier (SSID) ist der Name, der ein bestimmtes drahtloses lokales Netzwerk (WLAN) kennzeichnet. Bei der Suche nach verfügbaren drahtlosen Netzwerken wird dieser Name in der Liste angezeigt. <BR>
<BR>
<STRONG>Automatische Kanalwahl</STRONG><BR>
Wenn diese Option gewählt ist, sucht der Router automatisch den Kanal mit den geringsten Interferenzen und verwendet diesen für das drahtlose Netzwerk. Wenn Sie diese Option deaktivieren, verwendet der Router den Kanal, den Sie mit der Option Kanal angeben.
