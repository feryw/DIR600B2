<strong>Nützliche Hinweise</strong><br>
<br>
&nbsp;&#149;&nbsp; Zeitpläne werden mit einer Reihe anderer Funktionen verwendet, um festzulegen, wann diese Funktionen ausgeführt werden sollen.<br><br>
&nbsp;&#149;&nbsp; Geben Sie jedem Zeitplan einen aussagekräftigen Namen. Beispielsweise kann ein Zeitplan von Montag bis Freitag, 15 bis 21 Uhr 
„Nach der Schule“ heißen.<br><br>
&nbsp;&#149;&nbsp; Klicken Sie auf <strong>„Speichern“</strong>, um einen vollständigen Zeitplan zur darunter stehenden Liste hinzuzufügen.<br><br>
&nbsp;&#149;&nbsp; Klicken Sie auf das Symbol <strong>„Bearbeiten“</strong>, um einen vorhandenen Zeitplan zu ändern.<br><br>
&nbsp;&#149;&nbsp; Klicken Sie auf das Symbol <strong>„Löschen“</strong>, um einen Zeitplan endgültig zu löschen.