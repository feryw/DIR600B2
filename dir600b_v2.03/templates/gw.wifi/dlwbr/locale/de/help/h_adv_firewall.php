<strong>Nützliche Hinweise</strong>
<br><br>
&nbsp;&#149;&nbsp; <strong>DMZ:</strong><br>
Aktivieren Sie die Option DMZ nur als letzten Ausweg. Wenn Sie Schwierigkeiten 
haben, eine Anwendung von einem Computer hinter dem Router auszuführen, versuchen 
Sie zunächst, die entsprechenden Ports für die Anwendung im Abschnitt „Erweiterte 
Portweiterleitung“ zu öffnen.
<br><br>
&nbsp;&#149;&nbsp; <strong>Firewall:</strong><br>
Mit der erweiterten Funktion „Firewall-Regeln“ kann der Datenverkehr über den 
Router gestattet oder verweigert werden. Sie können jedoch genauere Zugriffsregeln 
einführen. Nähere Informationen und Beispiele finden Sie im Benutzerhandbuch.
