<strong>Nützliche Hinweise</strong>
<br>
<p>&nbsp;&#149;&nbsp;Aktivieren:<br>
Gibt an, ob der Eintrag aktiviert oder deaktiviert sein soll.</p>

<p>&nbsp;&#149;&nbsp;Schnittstelle:<br>
Legt die Schnittstelle (WAN oder WAN (physikalisch)) fest, die das 
IP-Paket verwenden muss, um bei Verwendung dieser Route den Router 
zu verlassen.</p>

<p>&nbsp;&#149;&nbsp;Ziel-IP-Adresse:<br>
IP-Adresse der Pakete, die diese Route verwenden.</p>

<p>&nbsp;&#149;&nbsp;Subnetzmaske:<br>
Ein-Bit in der Maske geben an, welche Bit der IP-Adresse übereinstimmen müssen.</p>

<p>&nbsp;&#149;&nbsp;Gateway:<br>
Gibt den nächsten Punkt (Hop) an, der bei Verwendung dieser Route angesprungen wird. 
Wenn die Adresse 0.0.0.0 als Gateway verwendet wird, bedeutet dies, dass es keinen 
nächsten Hop gibt und die passende IP-Adresse direkt mit dem Router auf der 
angegebenen Schnittstelle verbunden ist: WAN oder WAN (physikalisch).</p>