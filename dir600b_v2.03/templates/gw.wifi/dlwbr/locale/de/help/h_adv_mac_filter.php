<strong>Nützliche Hinweise</strong>
<br><br>
&nbsp;&#149;&nbsp; Erstellen Sie eine Liste mit MAC-Adressen, denen Sie den Zugriff auf Ihr Netzwerk gestatten oder verweigern möchten.
<br><br>
&nbsp;&#149;&nbsp; Computer, die eine IP-Adresse vom DHCP-Server des Routers bezogen haben, werden in 
der Liste der DHCP-Clients angezeigt. Wählen Sie ein Gerät aus dem Dropdown-Menü und klicken Sie auf die 
Pfeilschaltfläche, um die MAC-Adresse dieses Geräts zur Liste hinzuzufügen.
<br><br>
&nbsp;&#149;&nbsp; Über das Kontrollkästchen auf der linken Seite können Sie einen bestimmten Eintrag aktivieren bzw. deaktivieren.
<br><br>
&nbsp;&#149;&nbsp; Wenn Sie bereits vorher einen Zeitplan im Router definiert haben, können Sie diesen im Dropdown-Menü <strong>„Immer“</strong> auswählen.
Andernfalls klicken Sie auf <strong>„Hinzufügen“</strong>, um einen Zeitplan hinzuzufügen.
