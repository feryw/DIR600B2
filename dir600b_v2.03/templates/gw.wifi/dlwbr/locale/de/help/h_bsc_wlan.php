<strong>Nützliche Hinweise</strong><br>
<br>
&nbsp;&#149;&nbsp; Wi-Fi Protected Setup bietet einen intuitiveren Weg, die drahtlose Sicherheit zwischen dem 
Router und dem drahtlosen Client einzurichten. Stellen Sie sicher, dass die drahtlose Netzwerkkarte 
diese Funktion unterstützt oder einen zertifizierten Windows Vista-Treiber verwendet, um die Vorteile 
dieser Funktion zu nutzen.
<br><br>
&nbsp;&#149;&nbsp; Der erste Schritt zur Sicherung Ihres drahtlosen Netzwerks besteht darin, den Namen 
Ihres drahtlosen Netzwerks zu ändern. Wir empfehlen, einen geläufigen Namen zu wählen, der keine persönlichen 
Informationen enthält.
<br><br>
&nbsp;&#149;&nbsp; Eine weitere Möglichkeit zur Sicherung Ihres Netzwerks besteht darin, den verborgenen Modus 
zu aktivieren. Wenn diese Option aktiviert ist, können drahtlose Clients, die nach verfügbaren Netzwerken suchen, 
Ihr verborgenes Netzwerk nicht erkennen. Um die drahtlosen Geräte mit Ihrem Router zu verbinden, müssen Sie den 
Namen des drahtlosen Netzwerks auf jedem Gerät manuell eingeben.
<br><br>
&nbsp;&#149;&nbsp; Wenn Sie WLAN-Sicherheit aktiviert haben, schreiben Sie unbedingt den konfigurierten 
WEP-Schlüssel bzw. die Passphrase auf. Sie müssen diese Informationen an jedem drahtlosen Gerät eingeben, das 
Sie mit Ihrem drahtlosen Netzwerk verbinden möchten.
