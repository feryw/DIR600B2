<strong>Nützliche Hinweise</strong>
<br><br>
&nbsp;&#149;&nbsp; Im Dropdown-Menü <strong>„Anwendungsname“</strong> können Sie 
aus einer Liste vordefinierter Anwendungen wählen. Wenn Sie eine der vordefinierten 
Anwendungen wählen, klicken Sie auf die Pfeilschaltfläche neben dem Dropdown-Menü, 
um die entsprechenden Felder auszufüllen.
<br><br>
&nbsp;&#149;&nbsp; Sie können Ihren Computer aus der Liste der DHCP-Clients im 
Dropdown-Menü <strong>„Computername“</strong> auswählen oder manuell die IP-Adresse 
des Computers eingeben, zu dem Sie den angegebenen Port öffnen möchten.
<br><br>
&nbsp;&#149;&nbsp; Mit dieser Funktion können Sie einen Portbereich zu einem Computer 
in Ihrem Netzwerk öffnen. Geben Sie hierzu den ersten Port des auf dem Router zu 
öffnenden Bereichs in das erste Feld unter <strong>„Öffentlicher Port“</strong> 
und den letzten Port in das zweite Feld ein. Geben Sie anschließend den ersten Port 
des Bereichs, den der interne Server verwendet, im ersten Feld unter 
<strong>„Privater Port“</strong> und den letzten Port in das zweite Feld ein.
<br><br>
&nbsp;&#149;&nbsp; Sie können diese Funktion mit einem einzelnen Port verwenden, 
indem Sie einfach in beide Felder dieselbe Zahl eingeben. 