<p>
WEP ist der Verschlüsselungsstandard für drahtlose Verbindungen. 
Um diese Verschlüsselung zu verwenden, müssen Sie denselben bzw. 
dieselben Schlüssel für den Router und die drahtlosen Stationen angeben. 
Für Schlüssel mit einer Länge von 64 Bit müssen in jedes Feld 10 Hexadezimalzeichen 
eingegeben werden. Für Schlüssel mit einer Länge von 128 Bit müssen in jedes 
Feld 26 Hexadezimalzeichen eingegeben werden. Eine Hexadezimalzahlstelle ist entweder 
eine Zahl zwischen 0 und 9 oder ein Buchstabe von A bis F. Die größtmögliche Sicherheit 
mit WEP erhalten Sie, wenn Sie den Authentifizierungstyp „Gemeinsamer Schlüssel“ wählen.
</p>
<p>
Sie können auch eine beliebige Textzeichenfolge in das Feld für den WEP-Schlüssel eingeben. 
In diesem Fall wird die Zeichenfolge anhand der ASCII-Werte der Zeichen in einen hexadezimalen 
Schlüssel umgewandelt. Für 64 Bit-Schlüssel können maximal 5 Textzeichen eingegeben werden, 
für 128 Bit-Schlüssel maximal 13.
</p>
