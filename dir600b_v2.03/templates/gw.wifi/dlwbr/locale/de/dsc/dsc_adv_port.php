<h1>Regeln für erweiterte Portweiterleitung</h1>
Mit der Option „Erweiterte Portweiterleitung“ können Sie festlegen, dass ein einzelner 
öffentlicher Port des Routers auf eine interne IP-Adresse im LAN oder, falls erforderlich, 
auf einen privaten LAN-Port weitergeleitet wird. Dies ist hilfreich beim Hosten von 
Onlinediensten wie FTP- oder Webservern.
