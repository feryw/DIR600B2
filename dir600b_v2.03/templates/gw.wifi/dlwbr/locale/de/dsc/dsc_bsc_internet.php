<h1>Internetverbindung</h1>
Wenn Sie das Gerät zum ersten Mal konfigurieren, empfehlen wir, auf 
die Schaltfläche „Setup-Assistent für den Internetzugang“ zu klicken 
und den Anweisungen auf dem Bildschirm zu folgen. Wenn Sie die 
Geräteeinstellungen manuell ändern oder konfigurieren möchten, 
klicken Sie auf „Manuelles Einrichten der Internetverbindung“.
