<h1>Netzwerkeinstellungen</h1>
<p>
In diesem Abschnitt können Sie die Routereinstellungen für das interne Netzwerk sowie den 
integrierten DHCP-Server konfigurieren, um den Computern in Ihrem Netzwerk IP-Adressen 
zuzuweisen. Mit der hier eingerichteten IP-Adresse greifen Sie auf das webbasierte 
Verwaltungsprogramm zu. Wenn Sie diese IP-Adresse ändern, müssen Sie unter Umständen die 
Netzwerkeinstellungen der PCs anpassen, um weiterhin auf das Netzwerk zuzugreifen.
</p>
<p><b>
Beachten Sie, dass diese Einstellungen optional sind. 
Sie müssen hier keine Einstellungen vornehmen, um Ihr 
Netzwerk betriebsbereit zu machen.
</b></p>
