<h1>QoS (Quality of Service)</h1>
<p>
In diesem Abschnitt können Sie die QoS-Engine von D-Link konfigurieren. Mithilfe der QoS-Engine 
werden die VoIP-Qualität oder das Streaming verbessert, indem diese Dienste Priorität gegenüber 
anderem Datenverkehr erhalten, z. B. FTP oder Web. Aktivieren Sie die Option 
„Verzögerung eliminieren“, um eine bestmögliche Leistung zu erhalten.
So werden Ihren Anwendungen automatisch Prioritäten zugeteilt.
</p>
