<h1>Drahtlose Verbindung</h1>
<p>
Sie haben zwei Möglichkeiten, die Drahtlosverbindung 
einzurichten. Sie können entweder den Setup-Assistenten 
für die Drahtlosverbindung ausführen oder die 
Drahtlosverbindung manuell konfigurieren.
</p>
<p><b>
Beachten Sie, dass Änderungen in diesem Abschnitt auch auf Ihren drahtlosen Clients und dem PC vorgenommen werden müssen.
</b></p>
