<h1>Regeln für Anwendungen</h1>
Mit den Regeln für Anwendungen können Sie einzelne oder mehrere Ports in Ihrer Firewall öffnen, 
wenn der Router erkennt, dass Daten auf einem ausgehenden „Trigger“-Port oder einem 
Portbereich ins Internet gesendet werden. Regeln für Spezialanwendungen gelten für alle Computer 
im internen Netzwerk.
