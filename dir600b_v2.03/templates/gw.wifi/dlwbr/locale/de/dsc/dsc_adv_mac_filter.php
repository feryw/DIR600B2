<h1>MAC-FILTERUNG</h1>
Die Option zur MAC (Media Access Controller)-Addressfilterung dient der 
Netzwerk-Zugriffskontrolle auf Grundlage der MAC-Adresse des Netzwerkadapters. 
Eine MAC-Adresse ist eine eindeutige Kennung, die vom Hersteller des Netzwerkadapters 
vergeben wird. Diese Funktion kann konfiguriert werden, den Netzwerk- bzw. 
Internetzugang zu gestatten oder zu verweigern.
