<h1>Regeln für die Kindersicherung </h1>
<p>
Die Kindersicherung bietet Möglichkeiten, den Internetzugang zu beschränken. Mit Hilfe 
von „Website-URL“ können Sie eine Liste aller Websites erstellen, auf die Benutzer 
zugreifen bzw. nicht zugreifen dürfen. Über die Option „Zeitplan“ können Sie steuern, 
wann mit dem Router verbundenen Computern Zugriff auf das Internet erlaubt wird.
</p>
