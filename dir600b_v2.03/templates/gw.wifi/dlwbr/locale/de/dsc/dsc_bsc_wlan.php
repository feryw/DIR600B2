<h1>Drahtloses Netzwerk</h1>
In diesem Abschnitt können Sie die Einstellungen für das drahtlose Netzwerk 
Ihres D-Link-Routers konfigurieren. Beachten Sie, dass Änderungen in diesem 
Abschnitt unter Umständen auch auf Ihren drahtlosen Clients vorgenommen 
werden müssen.
<p>
Zum Schutz Ihrer Privatsphäre können Sie verschiedene Sicherheitsfunktionen für 
das Drahtlosnetzwerk konfigurieren. Dieses Gerät unterstützt die folgenden 
Sicherheitsmodi für drahtlose Netzwerke: WEP, WPA und WPA2.
