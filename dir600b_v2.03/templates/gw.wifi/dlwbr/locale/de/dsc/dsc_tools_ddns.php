<h1>Dynamisches DNS</h1>
Mit der Funktion DDNS (Dynamisches DNS) können Sie einen Server (Web, FTP, Spiele-Server usw.) über einen Domänennamen (z. B. www.einebeliebigedomaene.com) bereitstellen, der über Ihre dynamisch zugewiesene IP-Adresse erreichbar ist. Die meisten Breitband-Internetdiensteanbieter weisen dynamische (wechselnde) IP-Adressen zu. Mit Hilfe von DDNS können sich Ihre Freunde über Ihren Domänennamen mit Ihrem Server verbinden, völlig unabhängig davon, welche IP-Adresse dieser gerade verwendet.
