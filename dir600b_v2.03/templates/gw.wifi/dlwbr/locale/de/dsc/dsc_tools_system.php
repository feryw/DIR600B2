<h1>Einstellungen speichern und wiederherstellen</h1>
<p>
Wenn die Konfiguration des Routers abgeschlossen ist, können Sie die vorgenommenen Einstellungen in einer Konfigurationsdatei auf der Festplatte speichern. Sie haben auch die Möglichkeit, die Konfigurationseinstellungen zu laden oder die Standard-Werkseinstellungen wiederherzustellen.
</p>
