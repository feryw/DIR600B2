<h1>Internetverbindung</h1>
In diesem Abschnitt können Sie den Internetverbindungstyp konfigurieren. 
Sie können zwischen den folgenden Verbindungstypen wählen: Statische IP-Adresse, DHCP, PPPoE, PPTP, 
L2TP.
 Sollten Sie sich bei Auswahl nicht sicher sein, wenden Sie sich an Ihren 
Internetdiensteanbieter.
<br><br>
<strong>Hinweis: </strong> Wenn Sie die Option PPPoE wählen,
müssen Sie jegliche PPPoE-Clientsoftware von Ihren Computern entfernen bzw. deaktivieren.
