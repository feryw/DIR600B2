<h1>Uhrzeit und Datum</h1>
Mit der Option „Zeit- und Datumskonfiguration“ können Sie die korrekte Zeit für die interne Systemuhr konfigurieren, aktualisieren und verwalten.
In diesem Abschnitt können Sie Ihre lokale Zeitzone einstellen und den NTP-Server (Network Time Protocol) für das Netzwerkzeitprotokoll konfigurieren.
Die Einstellungen zum Umstellen auf Sommer- bzw. Winterzeit können hier ebenfalls konfiguriert werden.
