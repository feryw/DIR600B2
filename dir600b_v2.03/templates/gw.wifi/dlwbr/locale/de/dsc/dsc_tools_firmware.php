<h1>Firmware-Aktualisierung</h1>
Von Zeit zu Zeit gibt es eine neue Firmware-Version für den <?query("/sys/modelname");?>  um die Funktionalität und Leistung des Routers zu verbessern.<br>
<a href=<?query("/sys/supporturl");?> target=_blank>Klicken Sie hier, um auf unserer Support-Website nach einer Aktualisierung zu suchen.</a><br>
<br>
Zur Firmware-Aktualisierung klicken Sie auf die Schaltfläche „Durchsuchen“, und wählen Sie die Aktualisierungsdatei auf der lokalen Festplatte aus. 
Klicken Sie anschließend auf „Übernehmen“, um mit der Firmware-Aktualisierung zu beginnen.<br>
<br>
Mit dem Sprachpaket können Sie die Sprache der Benutzeroberfläche des <?query("/sys/modelname");?> festlegen.
 Wir empfehlen, bei der Aktualisierung der Firmware auch das Sprachpaket zu aktualisieren. Dies stellt sicher,
 dass alle Änderungen in der Firmware korrekt angezeigt werden.<br>
<br>
Suchen Sie zum Aktualisieren des Sprachpakets mithilfe der Schaltfläche "Durchsuchen" 
die Aktualisierungsdatei auf der lokalen Festplatte. Wenn Sie die Datei gefunden haben, klicken 
Sie auf die Schaltfläche "Hochladen", um die Aktualisierung des Sprachpakets zu starten.<br>
