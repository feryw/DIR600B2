<h1>Erweiterte WLAN-Einstellungen</h1>
<p>
Diese Optionen sind für Benutzer, die das Verhalten ihrer 802.11g-Drahtlosfunktion 
verändern möchten. Wir empfehlen, die Werkseinstellungen nicht zu ändern. Falsche 
Einstellungen können sich nachteilig auf die Leistung Ihrer Drahtlosfunktion auswirken. 
Die Standardeinstellungen sorgen in den meisten Fällen für die beste Leistung der 
Drahtlosfunktion.
</p>
