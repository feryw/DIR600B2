<h1>FIREWALL- UND DMZ-EINSTELLUNGEN</h1>
<p>
Mit Hilfe der Firewall-Regeln können Sie zulassen oder verhindern, dass Datenverkehr 
über den Router abgewickelt wird. Sie können einen einzelnen Port oben über das 
Eingabefeld festlegen oder einen Portbereich über beide Eingabefelder.
</p>
<p>
DMZ bedeutet „Demilitarisierte Zone“. Mit Hilfe von DMZ kann auf Computer hinter der 
Firewall eines Routers aus dem Internet zugegriffen werden. Üblicherweise enthält Ihre 
DMZ mehrere Webserver, FTP-Server und andere Server.
</p>
