<h1>Verbindung mit statischer IP-Adresse einrichten</h1>
<p>Um diese Verbindung einzurichten, benötigen Sie eine vollständige Liste der IP-Informationen von Ihrem Internetdiensteanbieter. 
Falls Sie über eine statische IP-Verbindung verfügen und keine Informationen hierzu besitzen, 
wenden Sie sich an Ihren Internetdiensteanbieter.</p>
