<h1>DHCP-Verbindung (dynamische IP-Adresse)</h1>
<p>Vergewissern Sie sich vor dem Einrichten dieser Verbindung, dass der D-Link-Router mit dem PC verbunden ist, der ursprünglich an Ihre Breitbandverbindung angeschlossen war. Wenn dies der Fall ist, klicken Sie auf die Schaltfläche „MAC-Adresse kopieren“, 
um die MAC-Adresse Ihres Computers auf den D-Link-Router zu kopieren.</p>
