<strong>Astuces...</strong>
<br><br>
&nbsp;&#149;&nbsp; Le menu déroulant <strong>Nom d'application</strong> contient 
une liste d'applications prédéfinies dans laquelle vous pouvez faire votre choix. 
Si vous sélectionnez une application prédéfinie, cliquez sur la flèche en regard 
du menu déroulant pour renseigner les champs nécessaires.
<br><br>
&nbsp;&#149;&nbsp; Vous pouvez choisir votre ordinateur dans la liste des clients 
DHCP du menu déroulant <strong>Nom d'ordinateur</strong> ou saisir manuellement 
l'adresse IP du PC sur lequel vous souhaitez ouvrir le port spécifié.
<br><br>
&nbsp;&#149;&nbsp; Cette fonctionnalité permet d'ouvrir une plage de ports sur un 
ordinateur de votre réseau. Pour ce faire, saisissez le premier port de la plage 
à ouvrir sur le routeur dans la première zone sous <strong>Port public</strong> et le dernier port 
de la plage dans la seconde zone. Saisissez ensuite le premier port de la plage 
utilisée par le serveur interne dans la première zone sous <strong>Port privé</strong> et le dernier 
port de la plage dans la seconde zone.
<br><br>
&nbsp;&#149;&nbsp; Pour ouvrir un seul port avec cette fonction, 
saisissez le même numéro dans les deux zones. 
