<strong>Astuces...</strong>
<br>
<p>&nbsp;&#149;&nbsp;Activer:<br>
Indique si l'élément est activé ou désactivé.</p>

<p>&nbsp;&#149;&nbsp;Interface:<br>
Définit l'interface -- WAN ou WAN physique -- que le paquet IP doit utiliser 
pour sortir du routeur, lorsque cette route est utilisée.</p>

<p>&nbsp;&#149;&nbsp;IP cible:<br>
Adresse IP des paquets qui empruntent cette route.</p>

<p>&nbsp;&#149;&nbsp;Masque de sous-réseau:<br>
Les 1 du masque correspondent aux bits de l'adresse IP à conserver.</p>

<p>&nbsp;&#149;&nbsp;Passerelle:<br>
Indique le prochain saut si cette route est utilisée. Dans une passerelle 0.0.0.0, 
il n'y a pas de saut et l'adresse IP correspondante est directement connectée au 
routeur de l'interface définie : WAN ou WAN physique.</p>
