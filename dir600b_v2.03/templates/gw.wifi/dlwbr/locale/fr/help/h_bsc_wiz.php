<STRONG>Paramètres de connexion sans fil</STRONG><BR>
La section sur les paramètres de connexion sans fil vous aidera à configurer la partie point d'accès sans fil du routeur. Notez que certaines modifications de cette section doivent être reproduites sur les périphériques client sans fil. <BR>
<BR>
<STRONG>Radio sans fil</STRONG><BR>
Cette option active ou désactive la fonction de connexion sans fil du routeur. Lorsque la radio est activée, les paramètres sans fil suivants s’affichent. <BR>
<BR>
<STRONG>SSID</STRONG><BR>
Service Set Identifier (SSID) est le nom d'un réseau local sans fil (WLAN) spécifique. Lorsqu'un périphérique sans fil recherche les réseaux sans fil disponibles, il s'agit du nom qui apparaît dans la liste. <BR>
<BR>
<STRONG>Sélection automatique du canal</STRONG><BR>
Si vous sélectionnez cette option, le routeur recherche automatiquement le canal qui présente le moins d'interférences pour y établir le réseau sans fil. Si vous la désactivez, le routeur utilise le canal indiqué par l'option Canal.
