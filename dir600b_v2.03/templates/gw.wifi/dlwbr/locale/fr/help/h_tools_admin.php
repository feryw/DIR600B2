&nbsp;&#149;&nbsp; Pour des raisons de sécurité, il est conseillé de changer le nom de connexion 
et le mot de passe des comptes administrateur et utilisateur. Notez soigneusement les nouveaux noms 
de connexion et mots de passe pour ne pas être obligé de réinitialiser le routeur en cas d'oubli.
<br><br>
&nbsp;&#149;&nbsp; Lorsque vous activez la gestion à distance, vous pouvez indiquer l'adresse IP de 
l'ordinateur sur Internet pour lequel vous souhaitez autoriser l'accès à votre routeur ou indiquer 
un blanc pour autoriser l'accès à tous les ordinateurs sur Internet.
