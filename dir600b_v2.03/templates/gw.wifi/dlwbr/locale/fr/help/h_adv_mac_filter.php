<br><br>
&nbsp;&#149;&nbsp; Créez une liste d’adresses MAC auxquelles vous souhaitez autoriser ou refuser l’accès à votre réseau.
<br><br>
&nbsp;&#149;&nbsp; Les ordinateurs ayant obtenu une adresse IP auprès du serveur DHCP du routeur 
figurent dans la liste des clients DHCP. Sélectionnez un périphérique dans le menu déroulant et 
cliquez sur la flèche pour ajouter son adresse MAC à la liste.
<br><br>
&nbsp;&#149;&nbsp; Utilisez la case à cocher située à gauche pour activer ou désactiver un élément.
<br><br>
&nbsp;&#149;&nbsp; Utilisez le menu déroulant <strong>Toujours</strong> si vous avez défini une programmation sur le routeur.
Si ce n'est pas le cas, cliquez sur le bouton <strong>Ajouter nouvelle</strong> pour en ajouter une.
