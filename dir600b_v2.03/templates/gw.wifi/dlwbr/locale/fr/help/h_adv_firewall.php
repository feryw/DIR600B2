&nbsp;&#149;&nbsp; <strong>DMZ:</strong><br>
N'activez l'option DMZ qu'en dernier ressort. Si vous rencontrez des difficultés 
pour utiliser une application sur un ordinateur placé derrière le routeur, 
commencez par essayer d'ouvrir les ports associés à l'application dans la 
section Redirection avancée de port.
<br><br>
&nbsp;&#149;&nbsp; <strong>Firewall:</strong><br>
Règles du firewall est une fonctionnalité avancée utilisée pour refuser ou 
autoriser le passage du trafic par l'appareil. Vous pouvez créer des règles 
détaillées pour l'appareil. Reportez-vous au manuel pour plus de détails et des exemples.
