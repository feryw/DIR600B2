&nbsp;&#149;&nbsp; La technologie Wi-Fi Protected Setup offre une méthode encore plus intuitive pour 
configurer la sécurité sans fil entre le routeur et le client sans fil. Vérifiez que la carte sans 
fil prend en charge cette fonction ou utilise un pilote certifié Windows Vista pour en profiter.
<br><br>
&nbsp;&#149;&nbsp; Pour sécuriser votre réseau sans fil, la première étape consiste à en modifier le nom. 
Nous vous conseillons de choisir un nom familier ne contenant aucune information personnelle.
<br><br>
&nbsp;&#149;&nbsp; Pour protéger votre réseau, il est également possible d'activer le mode de dissimulation. 
Lorsque cette option est activée, aucun client sans fil ne peut voir votre réseau sans fil lors d'un balayage 
visant à repérer les réseaux disponibles. Pour permettre aux périphériques sans fil de se connecter à votre 
routeur, vous devez saisir manuellement le nom du réseau sans fil sur chacun d’entre eux.
<br><br>
&nbsp;&#149;&nbsp; Si vous avez activé la sécurité sans fil, notez soigneusement la clé WEP ou la phrase 
de passe configurée. En effet, il sera nécessaire d’entrer ces informations sur chaque périphérique sans 
fil que vous souhaitez connecter à votre réseau sans fil.
