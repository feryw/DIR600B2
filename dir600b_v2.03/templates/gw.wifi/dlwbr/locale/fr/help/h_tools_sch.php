&nbsp;&#149;&nbsp; Les programmations sont utilisées avec d'autres fonctionnalités, afin de déterminer les cas d'activation de ces fonctions.<br><br>
&nbsp;&#149;&nbsp; Donnez à chaque programmation un nom parlant pour vous. 
Par exemple, une programmation du lundi au vendredi, de 15 h à 21 h, peut être baptisée Après l'école.<br><br>
&nbsp;&#149;&nbsp; Cliquez sur <strong>Enregistrer</strong> pour ajouter une programmation complétée à la liste ci-dessous.<br><br>
&nbsp;&#149;&nbsp; Cliquez sur <strong>l'icône Modifier</strong> pour changer une programmation existante.<br><br>
&nbsp;&#149;&nbsp; Cliquez sur <strong>l'icône Supprimer</strong> pour supprimer définitivement une programmation.
