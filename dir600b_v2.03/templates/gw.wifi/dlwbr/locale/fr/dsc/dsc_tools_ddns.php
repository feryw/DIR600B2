<h1>DNS dynamique</h1>
La fonction DNS dynamique permet d’héberger un serveur (Web, FTP, serveur de jeu, etc.) au moyen d’un nom de domaine que vous avez acheté (www.votrenom.com), lorsque votre adresse IP est attribuée dynamiquement. La plupart des fournisseurs de services Internet haut débit attribuent des adresses IP dynamiques (variables). Avec un fournisseur de services DDNS, vos amis pourront utiliser votre nom d'hôte pour se connecter à votre serveur de jeu, quelle que soit votre adresse IP.
