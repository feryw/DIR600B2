<h1>Connexion Internet</h1>
Utilisez cette section pour configurer votre type de connexion Internet. 
Il existe plusieurs types de connexion possibles : IP statique, DHCP, PPPoE, PPTP, et L2TP. 
Si vous n'êtes pas sûr de la méthode que vous utilisez, contactez votre fournisseur d'accès à Internet.
<br><br>
<strong>Remarque: </strong> si vous utilisez l'option PPPoE,
vous devrez supprimer ou désactiver tout logiciel client PPPoE présent sur vos ordinateurs.
