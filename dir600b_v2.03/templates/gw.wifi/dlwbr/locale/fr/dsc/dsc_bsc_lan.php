<h1>Configuration du réseau</h1>
<p>
Cette section vous aidera à configurer les paramètres du réseau interne sur votre routeur 
ainsi que le serveur DHCP intégré pour assigner une adresse IP aux ordinateurs du réseau. 
L’adresse IP configurée ici est celle que vous utilisez pour accéder à l'interface de gestion Web. 
Si vous la modifiez, il sera peut-être nécessaire d’ajuster les paramètres réseau de votre PC pour 
pouvoir accéder à nouveau au réseau.
</p>
<p><b>
Remarque : cette section est facultative. 
Pour que votre réseau fonctionne, 
vous n'avez pas besoin de modifier des paramètres ici.
</b></p>
