<h1>CONFIGUREZ UNE CONNEXION BASEE SUR UN NOM D'UTILISATEUR ET UN MOT DE PASSE (PPTP)</h1>
<p>Pour configurer cette connexion, vous devez disposer d'un nom d'utilisateur et d’un mot 
de passe fournis par votre FAI. Vous aurez également besoin de l'adresse IP PPTP. 
Si vous ne disposez pas de cette information, contactez votre FAI.</p>
