<h1>Connexion Internet</h1>
Si c'est la première fois que vous configurez le périphérique, nous vous 
recommandons d'ouvrir l'Assistant de configuration de la connexion Internet 
et de suivre les instructions qui s'affichent. Pour configurer le périphérique 
ou modifier ses paramètres manuellement, cliquez sur Configuration manuelle de la connexion Internet.
