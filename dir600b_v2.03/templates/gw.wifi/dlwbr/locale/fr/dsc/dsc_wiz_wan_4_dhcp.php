<h1>Connexion DHCP (adresse IP dynamique)</h1>
<p>Pour configurer cette connexion, assurez-vous que vous êtes connecté au routeur D-Link avec 
le PC qui était initialement relié à votre connexion haut débit. Si c'est le cas, 
cliquez sur le bouton Cloner l'adresse MAC pour copier l'adresse MAC de votre ordinateur sur le routeur D-Link.</p>
