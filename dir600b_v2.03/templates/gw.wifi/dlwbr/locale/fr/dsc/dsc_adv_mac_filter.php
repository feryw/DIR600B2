<h1>FILTRAGE MAC</h1>
L'option de filtrage des adresses MAC (Media Access Controller) permet 
de contrôler l'accès au réseau à partir des adresses MAC des adaptateurs réseau. 
L'adresse MAC est un identifiant unique attribué à un adaptateur réseau par son 
constructeur. Cette fonctionnalité peut être configurée pour AUTORISER ou REFUSER 
l'accès au réseau ou à Internet.
