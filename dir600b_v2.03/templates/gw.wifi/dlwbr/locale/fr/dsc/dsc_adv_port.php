<h1>Règles de redirection avancée de port</h1>
L'option Redirection avancée de port permet de définir un port public 
unique sur votre routeur, pour la redirection du trafic vers une adresse 
IP du réseau local interne et un port de réseau local privé si nécessaire. 
Elle est utile pour les services d’hébergement en ligne (par exemple serveurs FTP ou Web).
