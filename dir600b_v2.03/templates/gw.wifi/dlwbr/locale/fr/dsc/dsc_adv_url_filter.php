<h1>Règles de contrôle parental </h1>
<p>
Le contrôle parental est un ensemble d'outils permettant de limiter efficacement l'accès à Internet. 
Vous pouvez créer rapidement la liste des URL de sites Web auxquels vous souhaiter autoriser ou refuser l'accès. 
En outre, grâce à l'option de programmation, vous pouvez définir des périodes pendant lesquelles les clients ou les ordinateurs connectés au routeur sont autorisés à se connecter à Internet.
</p>
