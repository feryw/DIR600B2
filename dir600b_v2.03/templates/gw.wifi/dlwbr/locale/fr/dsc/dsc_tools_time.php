<h1>Horodatage</h1>
L'option de configuration de la date et de l'heure permet de configurer, 
mettre à jour et gérer l'heure de l'horloge interne du système.
Dans cette section, vous pouvez définir votre fuseau horaire et le serveur NTP (Network Time Protocol).
Le passage à l'heure d'été peut également être spécifié pour changer l'heure à la date prévue.
