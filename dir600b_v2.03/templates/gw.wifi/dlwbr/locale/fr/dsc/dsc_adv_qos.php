<h1>QoS (qualité de service)</h1>
<p>
Cette section vous aidera à configurer le moteur de qualité de service de D-Link. 
Le moteur de qualité de service améliore la qualité de VoIP ou la diffusion continue 
en donnant à votre trafic VoIP ou continu la priorité sur les autres trafics du réseau, 
notamment FTP ou Web. Pour obtenir des performances optimales, cochez l'option 
"décalage éliminé" qui définit automatiquement la priorité pour vos applications.
</p>
