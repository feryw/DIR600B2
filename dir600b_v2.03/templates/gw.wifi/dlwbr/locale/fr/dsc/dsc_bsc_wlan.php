<h1>Réseau sans fil</h1>
Cette section vous aidera à configurer les paramètres sans fil de votre routeur D-Link. 
Notez que les modifications apportées dans cette section devront être reproduites sur le client sans fil.
<p>
Pour préserver votre confidentialité, vous pouvez configurer des fonctionnalités de sécurité sans fil. 
Ce périphérique prend en charge trois modes de sécurité sans fil dont WEP, WPA et WPA2.
