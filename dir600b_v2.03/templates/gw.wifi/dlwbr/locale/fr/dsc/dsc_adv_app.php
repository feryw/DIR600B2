<h1>Règle d'application</h1>
L'option Règles d'application permet d'ouvrir un ou plusieurs ports sur votre 
firewall lorsque le routeur détecte des données envoyées vers Internet via un 
port ou une plage de ports "déclencheurs" sortants. 
Les règles d’applications spéciales s'appliquent à tous les ordinateurs de votre réseau interne.
