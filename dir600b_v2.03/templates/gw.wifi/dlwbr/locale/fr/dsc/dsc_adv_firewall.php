<h1>PARAMÈTRES DU FIREWALL ET DMZ</h1>
<p>
Vous pouvez utiliser les Règles du firewall pour autoriser ou refuser le passage du trafic via le routeur.
Vous pouvez définir un port unique à l'aide de la zone de saisie supérieure 
ou une plage de ports à l'aide des deux zones de saisie.
</p>
<p>
DMZ signifie "Zone démilitarisée".
L'option DMZ permet aux ordinateurs placés derrière le firewall du routeur d'être exposés au trafic Internet.
En règle générale, une zone démilitarisée contient des serveurs Web, des serveurs FTP, etc.
</p>
