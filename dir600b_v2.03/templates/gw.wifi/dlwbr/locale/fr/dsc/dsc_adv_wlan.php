<h1>Paramètres avancés de connexion sans fil</h1>
<p>
Ces options permettent aux utilisateurs qui le souhaitent de modifier les paramètres 
standard de leur radio sans fil 802.11g. Nous vous recommandons de ne pas changer les 
valeurs d'usine par défaut. Une configuration incorrecte risquerait d'affecter les 
performances de la radio sans fil. Les paramètres par défaut fournissent les meilleures 
performances radio sans fil dans la plupart des environnements.
</p>
