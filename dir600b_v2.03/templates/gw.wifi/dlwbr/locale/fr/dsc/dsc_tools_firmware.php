<h1>Mise à jour du firmware</h1>
Un nouveau firmware est peut-être disponible pour améliorer les fonctionnalités et 
les performances de votre <?query("/sys/modelname");?>.<br>
<a href=<?query("/sys/supporturl");?> target=_blank>Cliquez ici pour vérifier l'existence d'une mise à niveau sur notre site d'assistance.</a><br>
<br>
Pour mettre à niveau le firmware, recherchez le fichier de mise à niveau sur le disque dur local à l'aide du bouton Parcourir. Une fois le fichier localisé, cliquez sur Enregistrer les paramètres pour lancer la mise à niveau du firmware.<br>
<br>
Le pack linguistique vous permet de changer la langue de l'interface utilisateur du <?query("/sys/modelname");?>. Nous vous conseillons de mettre à niveau le pack linguistique actuel si vous allez mettre à niveau le microprogramme. Ainsi, les mises à jour du microprogramme s'afficheront correctement.
<br>
<br>
Pour mettre à jour le pack linguistique, localisez le fichier de mise à niveau sur le disque dur local à l'aide du bouton Parcourir. Une fois que vous avez trouvé le fichier à utiliser, cliquez sur le bouton Télécharger pour lancer la mise à niveau du pack linguistique.
