<h1>Configurez une connexion de type adresse IP statique</h1>
<p>Pour configurer cette connexion, vous aurez besoin de la liste 
complète des informations IP fournies par votre FAI. 
Si vous disposez d'une connexion de type adresse IP statique et que 
vous ne possédez pas ces informations, contactez votre FAI.</p>
