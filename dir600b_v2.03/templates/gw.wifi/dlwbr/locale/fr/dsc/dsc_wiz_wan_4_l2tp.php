<h1>Configurez une connexion basée sur un nom d'utilisateur et un mot de passe (L2TP)</h1>
<p>Pour configurer cette connexion, vous devez disposer d'un nom d'utilisateur et d’un mot 
de passe fournis par votre FAI. Vous aurez également besoin de l'adresse IP L2TP. 
Si vous ne disposez pas de cette information, contactez votre FAI.</p>
