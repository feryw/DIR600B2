<p>
WEP est la norme de chiffrement sans fil.
Pour l'utiliser, vous devez définir la même clé sur le routeur et les stations sans fil.
Pour les clés de 64 bits, vous devez entrer 10 chiffres hexadécimaux dans chaque champ de clé. 
Pour les clés de 128 bits, vous devez entrer 26 chiffres hexadécimaux dans chaque champ de clé.
Un chiffre hexadécimal est soit un chiffre compris entre 0 et 9, soit une lettre comprise entre A et F.
Pour une sécurité maximale de la clé WEP, définissez le type d'authentification sur Clé partagée lorsque la fonction WEP est activée.
</p>
<p>
Vous pouvez également saisir une chaîne de texte dans une zone de clé WEP,
auquel cas le texte sera converti en une clé hexadécimale en utilisant les valeurs ASCII des caractères.
Pour les clés de 64 bits, 5 caractères maximum doivent être saisis,
et 13 caractères maximum pour les clés de 128 bits.<br>
</p>
