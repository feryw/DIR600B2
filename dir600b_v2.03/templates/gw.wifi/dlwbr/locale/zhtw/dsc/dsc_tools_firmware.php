<h1>韌體更新</h1>
您可以透過一個更新版本的新版韌體來改善<?query("/sys/modelname");?>的功能及效能。<br>
<a href=<?query("/sys/supporturl");?> target=_blank>點選此處來查看在技術支援網站上的更新</a><br>
<br>
要更新韌體，點選瀏覽來指向在本地磁碟機內更新檔案的位置，一旦您找到要用的檔案，點選上傳的按鈕來開始韌體更新。<br>
<br>
語言包可讓您變更<?query("/sys/modelname");?>的設定介面，若您有更新韌體，建議您更新目前的語言包，這樣才能確認在韌體內任何變更能正確的顯示。<br>
<br>
要更新語言包，點選瀏覽來指向在本地磁碟機內更新檔案的位置，一旦您找到要用的檔案，點選上傳的按鈕來開始語言包更新。<br>
