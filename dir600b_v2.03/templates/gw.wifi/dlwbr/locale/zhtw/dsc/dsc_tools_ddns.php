<h1>動態 DNS</h1>
動態 DNS 功能可使您使用已購買的網域名 (如 www.whateveryournameis.com)
搭配動態指定的 IP 位址，裝載 (host) 伺服器 (Web、FTP、遊戲伺服器等)。
大多數寬頻 Internet 服務提供者都是指定動態 (變動的) IP 位址。利用 DDNS 服務提供者，
您的朋友可以無視您目前的 IP 位址，進入您的主機名稱來連線遊戲伺服器。
