<strong>有用的提示…</strong><br>
<br>
&nbsp;&#149;&nbsp; 將 DMZ 選項視為最後手段啟用。 如果路由器後的某部電腦無法執行一應用程式，先在 [虛擬伺服器] 或 [服務埠轉傳] 區段中嘗試開始與應用程式相關的服務埠。<br>
<br>
&nbsp;&#149;&nbsp; 如果您試圖從路由器後使用 VPN，先確定已啟用 VPN 穿透 (passthrough)。<br>
<br>
&nbsp;&#149;&nbsp; 當使用中的 VPN 用戶端是在 VPN 相關的標準服務埠上運作時，VPN 穿透才會運作。如果 VPN 穿透已啟用，但無法從路由器後連線 VPN 用戶端，請聯繫網路管理者以找出是否使用了非標準的服務埠或選項。<br>
