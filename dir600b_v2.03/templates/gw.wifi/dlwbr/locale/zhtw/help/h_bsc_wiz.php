<strong>有用的提示…</strong><BR>
<BR>
<STRONG>無線設定</STRONG><BR>
<strong>無線設定</strong>區段用來組態路由器的無線(存取點)部分。
注意，此區段中所做的變更也必須和無線用戶端裝置配合。<BR><BR>

<STRONG>無線發射訊號</STRONG><BR>
此選項可開啟或關閉路由器的無線連線功能。開啟無線電時，會顯示以下無線參數。<BR>
<BR>
<STRONG>SSID</STRONG><BR>
服務組識別元 (SSID) 為辨識特定無線區域網路 (WLAN) 的名稱。當無線裝置正在瀏覽可用的無線網路時，
這是會出現在清單上的名稱。<BR>
<STRONG>自動頻道選取</STRONG><BR>
如果您選取此選項，路由器會自動找到頻道，並使用該頻道進行無線網路動作。
