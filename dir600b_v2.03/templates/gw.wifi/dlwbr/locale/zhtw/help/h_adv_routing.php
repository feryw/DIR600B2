<strong>有用的提示…</strong>
<br>
<p>&nbsp;&#149;&nbsp;啟用:<br>
指定該項目啟用或關閉。</p>

<p>&nbsp;&#149;&nbsp;介面:<br>
指定介面－WAN 或實體 WAN－也就是使用該路由時，IP 封包出走路由器所必須使用的介面。</p>

<p>&nbsp;&#149;&nbsp;目的IP:<br>
將接受此路由的封包之 IP 位址。</p>

<p>&nbsp;&#149;&nbsp;子網路遮罩:<br>
遮罩中的一位元(one-bit) 指定IP 位址必須符合哪些位元。</p>

<p>&nbsp;&#149;&nbsp;閘道:<br>
指定使用此路由時採用的下一個躍點 (hop)。0.0.0.0 的閘道意指沒有下一個躍點，
而符合的 IP 位址直接連線指定介面 (WAN 或 WAN 實體) 的路由器。</p>
