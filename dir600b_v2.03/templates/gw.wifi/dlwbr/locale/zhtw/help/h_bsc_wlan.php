&nbsp;&#149;&nbsp;<strong>Wi-Fi 保護的設定</strong>提供了在路由器與無線用戶端之間設定無線安全性的更直覺化方法。
<br><br>
&nbsp;&#149;&nbsp;變更無線網路名稱是保護無線網路的第一步。
我們建議您將之變更為一個熟悉的名稱，同時不要內含任何個人資訊。
<br><br>
&nbsp;&#149;&nbsp;啟用隱藏模式也是保護無線網路的另一個辦法。啟用此選項後，
無線用戶端進行掃描以查看提供什麼服務時，任何無線用戶端都無法看到您無線網路的內容。
若要讓無線裝置連線您的路由器，必須在每部裝置上手動輸入無線網路名稱。
<br><br>
&nbsp;&#149;&nbsp;如果您啟用了<strong>無線安全性</strong>，確定要寫下剛組態的WEP鑰匙或通行字組。
您必須在連線無線網路的任何無線裝置上輸入這項資訊。
