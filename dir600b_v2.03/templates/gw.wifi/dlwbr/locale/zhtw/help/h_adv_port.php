<strong>有用的提示…</strong>
<br><br>
&nbsp;&#149;&nbsp; 從下拉式選單核對<strong>應用名稱</strong>是否有何您需求的明確應用。
假如您從選單中選定一個明確的應用,點選下拉式選單旁的箭頭按鍵來完成適當的欄位。
<br><br>
&nbsp;&#149;&nbsp; 您可從<strong>電腦名稱</strong>下拉式選單的 DHCP 用戶端清單中選取您的電腦，
或是手動輸入開放特定服務埠的目的電腦之 IP 位址。
<br><br>
&nbsp;&#149;&nbsp; 此功能可使您開啟一服務埠範圍給網路上的一部電腦。
若要這麼做，在<strong>公用服務埠</strong>的第一個方塊中輸入要開放範圍的第一個服務埠，
在第二個方塊中輸入最後一個服務埠。之後，您在<strong>私人服務埠</strong>的第一個方塊中輸入內部伺服器使用的第一個服務埠，
在第二方塊中輸入範圍的最後一個服務埠。
<br><br>
&nbsp;&#149;&nbsp; 要在此功能中開啟單一服務埠，在兩個方塊中輸入相同編號。 
