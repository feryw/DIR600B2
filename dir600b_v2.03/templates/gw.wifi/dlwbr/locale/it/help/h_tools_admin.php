<strong>Suggerimenti utili</strong>
<br><br>
&nbsp;&#149;&nbsp; Per motivi di sicurezza, si raccomanda di cambiare nome utente e password degli account amministratore e utente. Assicurarsi di annotare i nuovi nomi utente e le nuove password per evitare di dover ripristinare il router qualora venissero dimenticati.
<br><br>
&nbsp;&#149;&nbsp; Quando si abilita Gestione remota, ии possibile specificare l'indirizzo IP del computer connesso a Internet e autorizzato ad accedere al router. In alternativa lasciare il campo vuoto per consentire l'accesso a qualsiasi computer connesso a Internet.
