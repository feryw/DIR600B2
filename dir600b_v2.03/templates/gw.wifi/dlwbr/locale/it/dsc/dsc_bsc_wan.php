<h1>Connessione Internet</h1>
In questa sezione ии possibile configurare il tipo di connessione Internet. ии possibile scegliere tra diversi tipi di connessione: IP statico, DHCP, PPPoE, PPTP, L2TP e BigPond. Se non si ии certi del metodo di connessione, contattare l'Internet Service Provider.
<br><br>
<strong>Note: </strong> Nota: se si utilizza l'opzione PPPoE, sarид necessario rimuovere o disabilitare eventuale software client PPPoE installato nei computer in uso.
