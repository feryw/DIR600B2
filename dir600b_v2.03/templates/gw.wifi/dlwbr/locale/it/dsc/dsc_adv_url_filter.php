<h1>Regole per controllo parentale</h1>
<p>
Il controllo parentale fornisce strumenti utili per limitare gli accessi a Internet. La funzione URL sito Web consente di creare rapidamente un elenco di siti Web ai quali gli utenti possono o non possono accedere. La funzione Pianificazione consente di controllare quando i client o i PC connessi al router sono autorizzati ad accedere a Internet.
</p>
