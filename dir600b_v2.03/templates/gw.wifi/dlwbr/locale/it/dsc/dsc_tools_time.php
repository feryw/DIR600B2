<h1>Data e ora</h1>
L'opzione Configurazione data e ora consente di configurare, aggiornare e gestire l'ora dell'orologio interno del sistema.
In questa sezione ии possibile impostare il fuso orario di appartenenza e il server NTP (Network Time Protocol).
Se necessario, ии inoltre possibile configurare il passaggio all'ora legale.
