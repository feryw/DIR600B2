<h1>Беспроводное соединение</h1>
<p>
Существует два способа установки беспроводного соединения.
Во-первых, можно использовать Мастер установки беспроводного соединения.
Во-вторых, можно настроить беспроводное соединение вручную.
</p>
<p><b>
Пожалуйста, помните, что выполненные здесь настройки, возможно, придется продублировать для беспроводных клиентов и компьютеров.
</b></p>
