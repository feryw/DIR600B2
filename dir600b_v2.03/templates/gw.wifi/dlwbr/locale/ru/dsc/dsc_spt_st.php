 <td height=2><a name=22><strong>Статистика</strong></a><br>
 <?query("/sys/modelname");?> ведет статистику об обработанном им трафике. Существует возможность просмотреть количество пакетов, принятых и отправленных маршрутизатором через Интернет (WAN), LAN и Беспроводные интерфейсы.
