 <td height=2><a name=23><b>Активные сессии</b></a><br>
 Активная сессия отображает Источник и Назначение пакетов, проходящих через <?query("/sys/modelname");?>. <br>
 <strong><em>IP-адрес - </em></strong> IP-адрес Источника, отображающий откуда идут пакеты. <br>
 <strong><em>Сессия TCP - </em></strong> Отображает количество TCP-пакетов, отправленных с IP-адреса источника . <br>
 <strong><em>Сессия UDP - </em></strong> Отображает количество UDP-пакетов, отправленных с IP-адреса источника . <br>
 <strong><em>Протокол - </em></strong> Отображает тип пакетов, переданных между IP-адресами источника и назначения. <br>
 IP-адрес Источника, отображающий откуда идут пакеты. <br>
 Порт Источника - Отображает порт, используемый для передачи пакетов на IP-адрес Назначения. <br>
 IP-адрес Назначения - Отображает IP-адрес, на который направляются пакеты. <br>
 Порт назначения - Отображает порт, который используется для принятия пакетов с IP-адреса Источника.  


