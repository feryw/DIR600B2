 <td height=26><a name=21><b>Журнала</b></a><br>
 <?query("/sys/modelname");?> ведет журнал событий, происходящих на устройстве. Журнал содержит до 400 последних записей. Более новые записи будут записываться на место устаревших. <br>

 <strong><em>Первая страница - </em></strong> Кликните по этой кнопке для перехода к первой странице журнала. <br>
 <strong><em>Последняя страница - </em></strong> Кликните по этой кнопке для перехода к последней странице журнала. <br>
 <strong><em>Предыдущая страница - </em></strong> Перемещает на одну страницу назад. <br>
 <strong><em>Следующая страница - </em></strong> Перемещает на одну страницу вперед. <br>

 <strong><em>Очистить - </em></strong> Полностью очищает записи журнала. </td>
