<h1>Расширенные правила перенаправления портов</h1>
Опция расширенного перенаправления портов позволяет задать 
один публичный порт маршрутизатора для перенаправления на внутренний IP-адрес LAN и приватный LAN-порт, 
если это необходимо. Эта функция полезна для хостинга online сервиса, такого, как FTP- или Web-сервер.
