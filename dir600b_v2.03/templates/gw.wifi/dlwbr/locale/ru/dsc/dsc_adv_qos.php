<h1>QoS (Качество обслуживания)</h1>
<p>
Используйте этот раздел для настройки функции Smart QoS D-Link.
Эта функция улучшает качество передачи голоса VoIP и потокового видео,
гарантируя, что пакеты такого трафика будут иметь более высокий приоритет,
по сравнению с другим трафиком, таким как FTP- или Web-трафик. Для обеспечения
лучшей производительности поставьте, пожалуйста, галочку в поле "Устранение задержки",
что позволит автоматически задавать приоритеты для приложений. 
</p>
