<h1>DHCP-соединение (Динамический IP-адрес)</h1>
<p>Чтобы установить это соединение, убедитесь, пожалуйста, что к Маршрутизатору D-Link подключен ПК, который был изначально к широкополсному соединению.
Если это так, то нажмите кнопку Клонировать MAC-адрес, чтобы скопировать MAC-адрес компьютера на Маршрутизатор D-Link.</p>
