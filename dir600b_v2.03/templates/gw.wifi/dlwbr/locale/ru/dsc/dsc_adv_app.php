<h1>Правило приложения</h1>
Опция правил приложения используется для открытия одного или нескольких портов межсетевого экрана,
когда маршрутизатор принимает данные, отправленные в Интернет на порт или диапазон портов, указанный в поле "условия срабатывания".
Правила специальных приложений применяются ко всем компьютерам во внутренней сети.
