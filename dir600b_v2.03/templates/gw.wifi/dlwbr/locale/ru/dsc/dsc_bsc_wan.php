<h1>Интернет-соединение</h1>
Этот раздел предназначен для настройки типа Интернет-соединения.
Доступно несколько опций: Статический IP, DHCP, PPPoE, PPTP, L2TP и BigPond.
В случае сомнений, какой тип соединения необходимо выбрать, пожалуйста, свяжитесь с провайдером.
<br><br>
<strong>Примечание: </strong> При использовании опции PPPoE необходимо
отключить программное обеспечение PPPoE-клиента на компьютерах.
