<h1>ВРЕМЯ И ДАТА</h1>
Опция настройки ВРЕМЯ И ДАТА позволяет настроить, обновить 
и управлять внутренними настройками времени системы.
Эта страница позволяет установить часовой пояс и настроить NTP (Network Time Protocol)-сервер.
Также существует возможность задать настройки перехода на сезонное время.
