<h1>Правила родительского контроля </h1>
<p>
Функция Родительского контроля обеспечивает полезный инструмент для ограничения Интернет-доступа.
Поле URL Web-сайтов позволяет быстро создать список Web-сайтов, к которым необходимо разрешить или
запретить доступ пользователей. Поле Расписание позволяет управлять временем, когда компьютерам,
подключенным к маршрутизатору, будет разрешен доступ к Интернет.
</p>
