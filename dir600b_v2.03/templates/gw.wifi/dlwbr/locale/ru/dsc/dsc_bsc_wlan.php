<h1>Беспроводная сеть</h1>
Этот раздел поможет задать беспроводные настройки маршрутизатора D-Link.
Пожалуйста, помните, что выполненные здесь настройки, возможно, придется
продублировать для беспроводного клиента.
<p>
Для защиты сети существует возможность настроить функции безопасности.
Данное устройство поддерживает три протокола шифрования: WEP, WPA, WPA2.
<p>
