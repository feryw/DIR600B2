<h1>Фильтрация на основе МАС-адресов</h1>
Фильтрация на основе МАС-адресов позволяет управлять доступом к сети на основе MAC-адресов сетевых адаптеров.
MAC-адрес - это уникальный идентификатор, назначаемый производителем адаптеров.
Настройка этой функции позволяет РАЗРЕШИТЬ или ЗАПРЕТИТЬ доступ к сети/Интернет.
