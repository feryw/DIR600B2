<strong>Sugerencias útiles.</strong>
<br><br>
&nbsp;&#149;&nbsp; Cree una lista de direcciones MAC a las que desearía permitir o denegar el acceso a la red.
<br><br>
&nbsp;&#149;&nbsp; Los ordenadores que han obtenido una dirección IP del servidor DHCP del router estarán en la lista de clientes DHCP. Seleccione un dispositivo del menú desplegable y haga clic en la flecha para añadir a la lista la dirección MAC de dicho dispositivo.
<br><br>
&nbsp;&#149;&nbsp; Utilice la casilla de verificación de la izquierda para activar o desactivar una entrada concreta.
<br><br>
&nbsp;&#149;&nbsp; Utilice el menú desplegable <strong>siempre</strong> si ha definido con anterioridad un programa en el router.
Si no es así,   haga clic en el botón <strong>Añadir nuevo</strong> para añadir uno.
