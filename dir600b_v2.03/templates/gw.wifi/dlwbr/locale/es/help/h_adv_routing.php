<strong>Sugerencias útiles.</strong>
<br>
<p>&nbsp;&#149;&nbsp;Activar::<br>
Especifica si la entrada estará habilitada o deshabilitada.</p>

<p>&nbsp;&#149;&nbsp;Interfaz:<br>
Especifica la interfaz, WAN o WAN física, que debe utilizar el paquete IP para salir del router, cuando se utiliza este router.</p>

<p>&nbsp;&#149;&nbsp;IP de destino:<br>
La dirección IP de los paquetes que utilizarán esta ruta.</p>

<p>&nbsp;&#149;&nbsp;Máscara de subred:<br>
Unos bits en la máscara especifican los bits de la dirección IP que deben coincidir.</p>

<p>&nbsp;&#149;&nbsp;Puerta de enlace:<br>
Especifica el siguiente salto que se debe hacer si se utiliza esta ruta. Una puerta de enlace de 0.0.0.0 implica que no existe un salto siguiente y que la dirección IP que coincide está conectada directamente al router en la interfaz especificada: WAN o WAN física.</p>
