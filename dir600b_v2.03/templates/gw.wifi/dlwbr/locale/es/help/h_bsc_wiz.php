<strong>Sugerencias útiles.</strong><BR>
<BR>
<STRONG>Parámetros inalámbricos</STRONG><BR>
La sección Parámetros inalámbricos se utiliza para configurar la parte de AP (punto de acceso) inalámbrico del router. Tenga en cuenta que puede que algunos de los cambios realizados en esta sección deban coincidir también en los dispositivos de cliente inalámbricos. <BR>
<BR>
<STRONG>Radio inalámbrica</STRONG><BR>
Esta opción activa o desactiva la característica de conexión inalámbrica del router. Cuando se enciende la radio, se muestran los siguientes parámetros inalámbricos. <BR>
<BR>
<STRONG>SSID</STRONG><BR>
Identificador configurado de servicio (SSID) es el nombre que identifica una red de área local inalámbrica (WLAN) específica. Cuando un dispositivo inalámbrico explora en busca de redes inalámbricas disponibles, éste es el nombre que aparecerá en la lista.<BR>
<BR>
<STRONG>Selección automática de canal</STRONG><BR>
Si selecciona esta opción, el router busca automáticamente el canal con menos interferencias y utiliza dicho canal para la conexión en red inalámbrica. Si desactiva esta opción, el router utiliza el canal que ha especificado con la opción Canal.
