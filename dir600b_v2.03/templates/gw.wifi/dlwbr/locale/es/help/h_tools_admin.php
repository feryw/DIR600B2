<strong>Sugerencias útiles.</strong>
<br><br>
&nbsp;&#149;&nbsp; Por motivos de seguridad, se recomienda cambiar el nombre de inicio de sesión y la contraseña para las cuentas de administrador y usuario. Asegúrese de escribir los nuevos nombres de inicio de sesión y las contraseñas para evitar tener que reiniciar el router en caso de olvidarlos.
<br><br>
&nbsp;&#149;&nbsp; Al activar Gestión remota, puede especificar la dirección IP del ordenador en Internet que desea que tenga acceso a su router o dejarlo en blanco para permitir el acceso a cualquier ordenador en Internet.
