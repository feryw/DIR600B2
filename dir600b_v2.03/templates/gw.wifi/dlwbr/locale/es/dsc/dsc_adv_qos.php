<h1>QoS (Calidad de servicio)</h1>
<p>
Utilice esta sección para configurar el motor de QoS de D-Link. Este motor de QoS mejora la calidad de voz o la canalización VoIP garantizando que su tráfico VoIP o de canalización tiene prioridad sobre otro tráfico de la red, como FTP o Web. Para conseguir el mejor rendimiento, marque la opción "demora eliminada" para establecer automáticamente la prioridad de sus aplicaciones.
</p>
