<h1>FILTRADO MAC</h1>
La opción de filtro de dirección MAC (Controlador de acceso a medios) se utiliza para controlar el acceso a la red en función de la dirección MAC del adaptador de red. Una dirección MAC es un ID exclusivo asignado por el fabricante del adaptador de red. Esta característica se puede configurar para PERMITIR o DENEGAR el acceso a la red/Internet.
