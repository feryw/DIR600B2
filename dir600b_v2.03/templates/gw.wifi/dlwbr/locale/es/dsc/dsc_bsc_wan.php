<h1>Conexión a Internet</h1>
Utilice esta sección para configurar su tipo de conexión a Internet. Existen varios tipos de conexión entre los que elegir: IP estática, DHCP, PPPoE, PPTP, L2TP y BigPond. Si no está seguro del método de conexión, póngase en contacto con su proveedor de servicios de Internet.
<br><br>
<strong>Nota: si está utilizando la opción PPPoE, necesitará eliminar o desactivar cualquier software cliente PPPoE en sus ordenadores.
