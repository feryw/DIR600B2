<h1>Configuracióin de red</h1>
<p>
Utilice esta sección para configurar los parámetros de red interna de su router y configurar también el servidor DHCP incorporado para asignar direcciones IP a los ordenadores de su red. La dirección IP que está configurada aquí es la dirección IP que utiliza para acceder a la interfaz de gestión basada en la web. Si cambia aquí la dirección IP, puede que necesite ajustar los parámetros de red del PC para acceder de nuevo a la red.
</p>
<p><b>
Tenga en cuenta que esta sección es opcional y que no necesita cambiar ninguno de estos parámetros para que la red funcione.
</b></p>
