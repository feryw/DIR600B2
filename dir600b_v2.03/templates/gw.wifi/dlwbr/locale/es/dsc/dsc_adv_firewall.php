<h1>PARÁMETROS DE CORTAFUEGOS Y DMZ</h1>
<p>
Las reglas de cortafuegos se pueden utilizar para permitir o denegar el tráfico que pasa a través del router. Puede especificar un puerto único utilizando el cuadro de entrada de la parte superior o un rango de puertos utilizando ambos cuadros de entrada.
</p>
<p>
DMZ significa "Zona desmilitarizada". DMZ permite que los ordenadores que están detrás del cortafuegos del router sean accesibles al tráfico en Internet. Normalmente, la DMZ contiene servidores web, servidores FTP y otros.
</p>
