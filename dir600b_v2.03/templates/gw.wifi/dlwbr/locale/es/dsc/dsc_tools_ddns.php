<h1>DNS dinámico</h1>
La característica DNS dinámica permite alojar un servidor (Web, FTP, servidor de juegos, etc...) utilizando un nombre de dominio que haya adquirido (www.whateveryournameis.com) con su dirección IP asignada dinámicamente. La mayoría de los proveedores de servicios de Internet de banda ancha asignan direcciones IP dinámicas (que cambian). Al utilizar un proveedor de servicios de DDNS, sus amigos pueden introducir su nombre de host para conectarse a su servidor de juegos sin importar cuál es su dirección IP.
