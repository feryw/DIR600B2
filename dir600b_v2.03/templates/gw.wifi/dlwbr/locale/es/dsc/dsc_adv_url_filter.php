<h1>Normas de control paterno</h1>
<p>
Control paterno proporciona las herramientas útiles para restringir el acceso a Internet. La URL del sitio web permite crear rápidamente una lista de sitios web a los que desea permitir o denegar el acceso a los usuarios. El programa permite controlar cuándo se permite el acceso a Internet a los clientes o los PC conectados al router.
</p>
