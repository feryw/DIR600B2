<h1>Fecha Y HORA</h1>
La opción Configuración de fecha y hora permite configurar, actualizar y mantener la hora correcta en el reloj interno del sistema.
En esta sección puede definir la zona horaria en la que se encuentra y definir el servidor NTP (Protocolo de hora de red).
También puede configurar el horario de verano para que se ajuste la hora cuando corresponda
