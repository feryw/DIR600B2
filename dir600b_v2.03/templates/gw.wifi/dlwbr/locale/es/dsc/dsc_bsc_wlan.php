<h1>Red inalámbrica</h1>
Utilice esta sección para configurar los parámetros inalámbricos para su router D-Link. Tenga en cuenta que también será necesario duplicar los cambios realizados en esta sección en su cliente inalámbrico.
<p>
Para proteger su intimidad, puede configurar las características de seguridad inalámbrica. Este dispositivo admite tres modos de seguridad inalámbrica: WEP, WPA y WPA2.
