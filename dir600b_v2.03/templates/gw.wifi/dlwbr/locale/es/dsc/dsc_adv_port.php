<h1>Reglas de direccionamiento de puerto avanzado</h1>
La opción Direccionamiento de puerto avanzado permite definir un puerto público único en su router para el redireccionamiento a una dirección IP de LAN interna y a un puerto de LAN privado, si es necesario. Esta característica resulta útil para alojar servicios en línea como servidores de FTP o Web.
