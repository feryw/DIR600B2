<h1>Parámetros de Opciones avanzadas inalámbricas</h1>
<p>
Estas opciones son para usuarios que desean cambiar el comportamiento de su radio inalámbrica 802.11g de los parámetros estándar. No se recomienda cambiar estos parámetros del valor predeterminado de fábrica. Los parámetros incorrectos pueden afectar al rendimiento de su radio inalámbrica. Los parámetros predeterminados deben proporcionar el mejor rendimiento de la radio inalámbrica en la mayoría de los entornos.
</p>
