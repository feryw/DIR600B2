<strong>유용한 정보</strong><BR>
<BR>
<STRONG>무선 설정</STRONG><BR>
무선 설정에서 공유기의 AP 모드를 설정합니다.  변경된 무선 설정은 무선 클라이언트에서도 동일하게 해야 할 수도 있습니다.  <BR>
<BR>
<STRONG>무선</STRONG><BR>
공유기의 무선 기능을 켜고 끌 수 있습니다. 무선을 켰을 때,  다음의 무선 변수들이 표시됩니다.  <BR>
<BR>
<STRONG>SSID</STRONG><BR>
SSID(Service Set Identifier) 특정 무선 네트워크(WLAN: Wireless LAN)를 확인하는 이름입니다. 무선 장비가 사용가능한 무선 네트워크를 찾을 때, 검색 목록에 표시되는 이름입니다.  <BR>
<BR>
<STRONG>채널 자동 선택</STRONG><BR>
채널 자동 선택 기능을 사용하면 공유기가 간섭이 가장적은 채널을 자동으로 찾게 됩니다.  이 기능을 사용하지 않는 경우는 채널을 직접 선택하십시오.
