<h1>DDNS</h1>
DDNS 기능으로 내부에서 유동 IP 주소를 사용하더라도 고객의 도메인(예: www.whateveryournameis.com) 을 사용하여 웹, FTP, 게임 서버등의 서버 호스팅이 가능합니다. 대부분의 브로드밴드 인터넷 서비스 공급업체가 유동 IP 주소를 할당하고 있습니다. DDNS 서비스를 사용하면 서버가 유동 IP를 사용해도, 고정 IP를 사용해도 도메인 이름만으로 서버에 접속할 수 있습니다. 
