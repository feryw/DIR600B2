<p>
WEP는 무선 암호화 표준입니다. 
WEP를 사용하기 위해서는 공유기와 무선 클라이언트에 반드시 동일한 키를 입력하셔야 합니다. 
64 비트 키를 사용하실 경우, 16 진수 10 자리를 입력하십시오. 128 비트 키를 사용하실 경우, 16 진수 26 자리 값을 각각 입력하셔야 합니다. 
16진수 값은 0~9 까지의 숫자, A~F 까지의 문자로 구성됩니다. 
WEP가 설정될 때, 대부분의 인증 방식은 “공유키(Shared Key)” 방식입니다. 
</p>
<p>
ASCII 문자를 16진수 값으로 변경하여 WEP 키를 입력할 수 도 있습니다. 
64 비트에서는 최대 5 자의 텍스트 문자를, 128 비트에서는 최대 13 자의 텍스트 문자를 입력할 수 있습니다. <br>
</p>
